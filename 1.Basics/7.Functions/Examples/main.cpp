struct frame{
    double matrix[4][4];
};



void TMULT(frame* Tab, frame* Tbc, frame *res){
    for(int i=0; i<4; i++){
        for(int j=0; j<4; j++){
            res->matrix[i][j] = 0;
            for(int k=0; k<4; k++){
                res->matrix[i][j] += Tab->matrix[i][k] * Tbc->matrix[k][j];
            }
        }
    }
}


